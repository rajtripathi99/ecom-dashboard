"use client"

import * as React from "react"
import { Button } from "@/components/ui/button"
import { Checkbox } from "@/components/ui/checkbox"
import {
  DropdownMenu,
  DropdownMenuCheckboxItem,
  DropdownMenuContent,
  DropdownMenuGroup,
  DropdownMenuItem,
  DropdownMenuLabel,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu"
import { Input } from "@/components/ui/input"
import { Skeleton } from "@/components/ui/skeleton"
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "@/components/ui/table"
import {
  flexRender,
  getCoreRowModel,
  getFilteredRowModel,
  getPaginationRowModel,
  getSortedRowModel,
  useReactTable,
  type ColumnDef,
  type ColumnFiltersState,
  type SortingState,
  type VisibilityState,
} from "@tanstack/react-table"
import { ArrowUpDown, ChevronDown, MoreHorizontal, Search, User } from "lucide-react"

export type Customer = {
  id: number
  name: string
  email: string
  image: string
  totalOrders: number
}

// Helper function to generate customer data from user data
const generateCustomerFromUser = (user: any): Customer => {
  // Generate random number of orders between 1 and 50
  const totalOrders = Math.floor(Math.random() * 50) + 1
  
  return {
    id: user.id,
    name: `${user.firstName} ${user.lastName}`,
    email: user.email,
    image: user.image,
    totalOrders: totalOrders
  }
}

